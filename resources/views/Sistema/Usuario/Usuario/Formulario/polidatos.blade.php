        <p class="left-align light"> <strong> <em> Base De Datos: </em> </strong> Conjunto organizado de datos personales que sea objeto de tratamiento. </p>

        <p class="left-align light"> <strong> <em> Dato Personal: </em> </strong> Cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables. </p>

        <p class="left-align light"> <strong> <em> Datos Sensibles: </em> </strong> Se entiende por datos sensibles aquellos que afectan la intimidad del titular o cuyo uso indebido puede generar su discriminación, tales como origen racial o étnico, la orientación política, las convicciones religiosas o filosóficas, la pertenencia a sindicatos, organizaciones sociales, de derechos humanos o que promuevan intereses de cualquier partido político o que garanticen los derechos y garantías de los partidos políticos de oposición, así como los datos relativos a la salud, la vida sexual y los datos biométricos. No puede ser objeto de tratamiento excepto cuando el Titular haya dado su autorización a dicho tratamiento. </p>

        <p class="left-align light"> <strong> <em> Encargado del Tratamiento: </em> </strong> Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, realice el tratamiento de datos personales por cuenta del responsable del tratamiento. </p>

        <p class="left-align light"> <strong> <em> Habeas Data: </em> </strong> Es el derecho de cualquier persona de conocer, actualizar y rectificar las informaciones que se hayan recogido sobre ellas en bancos de datos y en archivos de entidades públicas y privadas. </p>

        <p class="left-align light"> <strong> <em> Responsable del Tratamiento: </em> </strong> Persona natural o jurídica, pública o privada, que por sí misma o en asocio con otros, decida sobre la base de datos y/o el Tratamiento de los datos. </p>

        <p class="left-align light"> <strong> <em> Titular: </em> </strong> Persona natural cuyos datos personales sean objeto de Tratamiento. </p>

        <p class="left-align light"> <strong> <em> Tratamiento: </em> </strong> Cualquier operación o conjunto de operaciones sobre datos personales, tales como la recolección, almacenamiento, uso, circulación o supresión de los mismos. </p>
